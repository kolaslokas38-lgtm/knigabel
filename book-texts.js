// Функция для генерации контента страницы книги
function generateBookContent(bookId, page) {
    const book = BOOK_TEXTS[bookId];
    if (!book) return ["Книга не найдена"];

    const totalPages = Math.ceil(book.content.length / 10); // 10 абзацев на страницу
    const startIndex = (page - 1) * 10;
    const endIndex = startIndex + 10;

    return book.content.slice(startIndex, endIndex);
}

